    private $morphy = null;
    private $language = 'ru';
    private $dictionaries = array('ru' => 'ru_RU', 'en' => 'en_EN');

    public function __construct()
    {
        $this->init();
    }


    private function init()
    {
        try {
            $this->morphy = new \phpMorphy(__DIR__ . '/../../../vendor/phpmorphy/dicts',
                $this->dictionaries[$this->language],
                array('storage' => PHPMORPHY_STORAGE_FILE)
            );
        } catch (phpMorphy_Exception $e) {
            $this->morphy = null;
        }
    }

    public function language($language = null)
    {
        if ($language && isset($this->dictionaries[$language]) && $language != $this->language) {
            $this->language = $language;
            $this->init();
        }

        return $this;
    }

    public function obscene($text, $source_unless_normalized = false)
    {
        $text = $this->normalize($text, $source_unless_normalized);
        $obscene_words = $this->obscene_words();
        $obscene = false;
        foreach ($text as $word) {
            if (in_array($word, $obscene_words)) {
                $obscene = true;
                break;
            }
        }


        if (!$obscene) {
            $obscene = !!\Text_Censure::parse(implode(' ', $text), "1", "", false);
        }

        return array('obscene' => $obscene, 'text' => $text);
    }

    public function normalize($text, $source_unless_normalized = false)
    {
        $normal_text = array();

        if (!$this->morphy) {
            return $normal_text;
        }

        $text = trim($text);
        $text = preg_replace('/[^A-Za-zА-Яа-яЁё\']/u', ' ', $text);
        $text = preg_replace('/\s+/', ' ', $text);
        $text = mb_strtoupper($text, 'UTF-8');
        $text = trim($text);
        $text = explode(' ', $text);

        if (empty($text)) {
            return $normal_text;
        }

        $normal_text = array();

        $result = $this->morphy->lemmatize($text);

        if (is_array($result)) {
            $stopwords = $this->stopwords();
            foreach ($result as $source => $word) {
                if (is_array($word)) {
                    foreach ($word as $form) {
                        if (!in_array($form, $stopwords)) {
                            $normal_text[$form] = $form;
                        }
                    }
                } else {
                    if ($source_unless_normalized && !in_array($source, $stopwords)) {
                        $normal_text[$source] = $source;
                    }
                }
            }
        }

        return $normal_text;
    }

